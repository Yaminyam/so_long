#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		n;
	char	*str;

	n = ft_strlen(s1) + ft_strlen(s2);
	str = (char *)malloc(n + 1);
	if (str == 0)
		return (0);
	ft_memcpy(str, s1, ft_strlen(s1));
	ft_memcpy(str + ft_strlen(s1), s2, ft_strlen(s2));
	str[n] = '\0';
	return (str);
}
